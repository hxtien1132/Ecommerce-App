const getAllProduct = asyncHandler(async (req, res) => {
  try {
    // Filtering
    const queryObj = { ...req.query };
    const excludeFields = ["page", "sort", "limit", "fields"];
    // queryObj bị xóa các query "page", "sort", "limit", "fields" nếu có
    excludeFields.forEach((el) => delete queryObj[el]);
    let queryStr = JSON.stringify(queryObj);
    // console.log("🚀 ~ file: productCtrl.js:71 ~ getAllProduct ~ queryStr:", queryStr);

    //khớp một trong những(gte|gt|lte|lt)
    // sẽ bị thay thế(ex: product?price[gte]=1000 => price: { '$gte': '1000' })
    queryStr = queryStr.replace(/\b(gte|gt|lte|lt)\b/g, (match) => `$${match}`);

    // trả về k có query
    let query = Product.find(JSON.parse(queryStr));
    console.log(JSON.parse(queryStr));

    // "page", "sort", "limit", "fields" gọi sau

    // Sorting
    if (req.query.sort) {
      const sortBy = req.query.sort.split(",").join(" ");
      query = query.sort(sortBy);
    } else {
      query = query.sort("-createdAt"); //nếu k truyền sort sẽ lấy sort(-createdAt)
    }

    // limiting the fields
    //product?fields=title,price,description
    if (req.query.fields) {
      const fields = req.query.fields.split(",").join(" ");
      //title, price, description =>  title price description
      query = query.select(fields);
    } else {
      query = query.select("-__v"); // == 0
    }

    // pagination
    const page = req.query.page;
    const limit = req.query.limit;
    const skip = (page - 1) * limit;
    console.log("page: " + page + " limit: " + limit);
    query = query.skip(skip).limit(limit);
    if (req.query.page) {
      const productCount = await Product.countDocuments();
      if (skip >= productCount) throw new Error("This Page does not exists");
    }
    const product = await query;
    res.json(product);
  } catch (error) {
    throw new Error(error);
  }
});

